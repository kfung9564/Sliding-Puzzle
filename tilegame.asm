FindTileCoords proc
	push ax
	push bx
	push si
	push di
	push cx
	push dx
		
	mov bx, 0 ; tile index
	
	mov si, 0  	; h counter
	mov di, 0	; v counter
	mov cx, 22 	; initial x coord
	mov dx, 22 	; initial y coord		

NextCoord:
	mov tileCoordX[bx], cx ; save x coord
	mov tileCoordY[bx], dx ; save y coord

	inc bx	; increment 2 byte size array
	inc bx		
	inc si
	add cx, 32
	
	cmp si, w
	JNE NextCoord
	
	inc di
	mov cx, 22 	; reset to inital x coord
	mov si, 0 	; reset h counter
	add dx, 32
	
	cmp di, h
	JNE NextCoord
	
	pop dx
	pop cx
	pop di
	pop si
	pop bx
	pop ax
	
	ret
endp FindTileCoords


DrawTiles proc
	push ax
	push bx
	push si
	push di
	push cx
	push dx
	
	mov bx, 0	; tile index
	
	mov si, 0  	; tile drawing counter

NextTile:
	mov di, 30	; tile width
	
	mov cx, tileCoordX[bx] 	; initial x coord
	mov dx, tileCoordY[bx] 	; initial y coord

	push di ; use di to get tile number
	mov di, tiles[bx]
	add di, tiles[bx]
	mov si, offset [tileArray+di]
	pop di

	push bx 	; save bx
	mov bx, 30	; use bx as y coord end
	add bx, dx
	add di, cx
	
drawTile:
	mov al, [si]	; load color from bitmap
	mov ah, 0ch 	; put pixel
	int 10h
	inc cx
	
	inc si
	mov al, [si]

	cmp cx, di
	JNE drawTile

	mov cx, di  			; reset to start of col
	sub cx, 30				; get back original x
	inc dx     				; next row

	cmp dx, bx
	JNE drawTile
	
	pop bx
	
	inc bx 	; increment 2 byte 
	inc bx
	
	mov ax, tiles[bx]			; use ax to hold curr tile number to cmp
	cmp ax, 26					; 26 end of puzzle tiles
	JNE NextTile
	
	pop dx
	pop cx
	pop di
	pop si
	pop bx
	pop ax
	
	ret
endp DrawTiles


