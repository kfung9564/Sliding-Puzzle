CheckWin proc
	push ax
	push bx
	push cx
	push dx
	
	mov ax, 0   ; counter
	mov bx, 0	; tiles index
	
	mov cx, tiles[bx]
	cmp cx, 1			; check if first tile is 1, if not automatically no win
	JNE noWin	

check:
	mov dx, tiles[bx] 	; dx save curr tile
	inc bx
	inc bx
	mov cx, tiles[bx]	; next tile in cx

	mov ax, tiles[bx]
	cmp ax, 0
	JE checkNext		; if at empty space, check if next is end
	
continue:
	cmp dx, cx
	JG noWin			; no win if curr is greater than next
	
	JMP check
	
checkNext:
	inc bx
	inc bx
	mov ax, tiles[bx]
	cmp ax, 26			; if end of array after empty then win
	JE win
	
	JMP continue

;------- On Win --------
win:	
	call PrintWinMessage
	call FindTileCoords		; fills tileCoordX, tileCoordY
	
	mov ax, 2h				; hide cursor to draw under it
	int 33h
	
	call DrawTiles			; draw tiles in position
	
	mov ax, 1h				; show cursor again
	int 33h 
	
waitForEsc:
	mov ah, 0
	int 16h			; get keystroke

	cmp ah, 01h		; keystroke for esc
	JNE waitForEsc
	; esc is pressed
	JMP restartGame
	
restartGame:
	; refresh tiles array for next game
	push bx
	
	mov bx, 0	; index
	mov cx, 0	; counter
	
resetTilesLoop:
	mov tiles[bx], 26		; set value back to 26
	inc bx
	inc bx
	
	inc cx
	cmp cx, 26
	JNE resetTilesLoop

	pop bx

	call clearScreen
	call Game
	
;-----------------------------
	
noWin:
	pop dx
	pop cx
	pop bx
	pop ax
	
	ret
endp CheckWin


