GeneratePuzzle proc
	push ax
	push bx
	push cx
	push dx
	push si
	push di
		
;find puzzleLength
	mov ax, 0	; sum
	mov bx, 0	; counter
	
getNumberTiles:
	add ax, w
	
	inc bx
	cmp bx, h
	JNE getNumberTiles
	
	mov tilesLength, ax 	; length number
	
	add ax, ax				; 2 byte index ptr
	
	mov puzzleLength, ax	; save sum
	
		
CreatePuzzle:
	push ax
	push bx
	push cx
	push si
	push di
	
	call GenerateSeed
	
	mov bx, 0 	; index
	mov si, puzzleLength ; last index

	mov ax, seed
	
fillPuzzle:
	; Expects: AX = The seed
	; Returns: AX = The randomly generated number
	
	call GenerateRandomNumber 
	push si
	push ax					; save generated number to be used as seen next loop
	push bx					; save index
	
	mov bx, 0
	mov dx, tilesLength

	; Expects: AX = The random number, BX = The smallest range, DX = The highest range
	; Returns: AX = The randomly generated number
	call ConvertRandomNumberToRange
	
	mov di, ax				; save curr generated number to be used
	
	pop bx
	pop ax
	
	
	; if tile already exists, go to beginning of loop and find again for same index
	mov si, di
	add si, si
	mov cx, filledTiles[si]	; check if exists
	
	pop si
	
	cmp cx, 26				; 26 if does not not exist
	JNE fillPuzzle			; if not 26, it exists, repeat loop to find another number
	
	; number does not exist on board
	push si
	mov si, di
	add si, si
	
	mov filledTiles[si], di
	mov tiles[bx], di

	pop si
	
	inc bx
	inc bx
	
	cmp bx, si
	JNE fillPuzzle
	
	; puzzle filled
	; refresh filledTiles array for next generation
	
	push bx
	
	mov bx, 0	; index
	mov cx, 0	; counter
	
arrayLoop:
	mov filledTiles[bx], 26		; set value back to 26
	inc bx
	inc bx
	
	inc cx
	cmp cx, 26
	JNE arrayLoop

	pop bx

	pop di
	pop si
	pop cx
	pop bx
	pop ax
	
	; An inversion is when a tile precedes another tile with a lower number on it.
	; If width is odd, then the number of inversions in a solvable situation is even.
	; If the grid width is even, and the blank on even row counting from the bottom then inversions in a solvable situation is odd.
	; If the grid width is even, and the blank on odd row counting from the bottom then inversions in a solvable situation is even.
	; http://www.cs.bham.ac.uk/~mdr/teaching/modules04/java2/TilesSolvability.html
	
	mov si, 0	; index of tile
	mov ax, 0	; counter for inversions
	
countInversions:
	mov bx, si
	mov cx, tiles[bx]
	
	cmp cx, 26
	JE checkSolvable		; end of array
	
compareLoop:
	; cx curr value
	inc bx
	inc bx
	mov dx, tiles[bx]  ; next value
	
	cmp dx, 26
	JE nextIndex			; end of array
	
	cmp dx, 0
	JE compareLoop
	
	cmp cx, dx
	JL compareLoop
	
	; curr greater than next, add count
	inc ax
	
	JMP compareLoop
	
nextIndex:
	inc si
	inc si
	
	JMP countInversions


checkSolvable:
	; check first cond, if w is odd, then inversions is event
	
	; check w is odd, w % 2 != 0
	push ax		; save ax, bx, registers used for mod
	push bx
	push dx

	mov dx, 0	; remainder
	mov ax, w	; ax number getting divided
	mov bx,	2	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	mov di, dx	; save remainder in di

	pop dx
	pop bx
	pop ax
	
	cmp di, 0
	JE nextCond
	
	; w is odd, check if inversions is even
	mov si, ax	; save number of inversions
	
	push ax		; save ax, bx, registers used for mod
	push bx
	push dx

	mov dx, 0	; remainder
	mov ax, si	; ax number getting divided
	mov bx,	2	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	mov di, dx	; save remainder in di

	pop dx
	pop bx
	pop ax
	
	cmp di, 0
	JE jumpSolvable		; if 0, inversion is even, solvable
	
	JMP notSolvable
	
jumpSolvable:
	JMP solvable
	
nextCond:
	; If the grid width is even, and the blank on even row counting from the bottom then inversions in a solvable situation is odd.
	; we know width is even from checking odd
	
	; ilterate array backwards. find start of the end then start counting 
	; every w is a row
	; 0 if on even row
	; 1 if on odd row
	; find empty tile with odd/even symbol
	; if 0, even row then inversions odd -> solvable
	; if 1, odd row, then inversions even -> solvable
	
	mov cx, 1				; odd/even symbol, start at odd
	mov dx, w				; count down to change row
	
	mov bx, puzzleLength	
goNext:	
	dec bx					; go next element from end
	dec bx 					; index of last element, length - 1 in 2 btye

	mov di, tiles[bx]
	cmp di, 26		; while it is still at end indicator, do not start count yet
	JE goNext

countTiles:
	; start counting 
	mov di, tiles[bx]
	
	cmp di, 0 				; check if empty tile
	JE checkInversions
	; not 0
	
	dec bx					; go next element from end
	dec bx 

	dec dx
	cmp dx, 0
	JNE countTiles
	; next row
	inc cx 					; add to symbol, if cx is odd, odd row, even , even row
	mov dx, w				; reset counter
	
	JMP countTiles			; repeat if not

checkInversions:
	; check if inversion even or odd
	mov si, ax	; save inversion

	push ax		; save ax, bx, registers used for mod
	push bx
	push dx

	mov dx, 0	; remainder
	mov ax, si	; ax number getting divided
	mov bx,	2	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	mov di, dx	; save remainder in di

	pop dx
	pop bx
	pop ax
	
	cmp di, 0
	JE inversionEven 	; inversion is even, row must be odd
	
	JMP inversionOdd 	; else inversion is odd, row must be even

inversionEven:
	; cx/row must be odd
	push ax		; save ax, bx, registers used for mod
	push bx
	push dx

	mov dx, 0	; remainder
	mov ax, cx	; ax number getting divided
	mov bx,	2	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	mov di, dx	; save remainder in di

	pop dx
	pop bx
	pop ax
	
	cmp di, 0
	JNE solvable	
	
	JMP notSolvable

inversionOdd:
	; cx/row must be even 
	push ax		; save ax, bx, registers used for mod
	push bx
	push dx

	mov dx, 0	; remainder
	mov ax, cx	; ax number getting divided
	mov bx,	2	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	mov di, dx	; save remainder in di

	pop dx
	pop bx
	pop ax
	
	cmp di, 0
	JE solvable
	
	JMP notSolvable
	
solvable:
	; puzzle is created
	JMP puzzleCreated

notSolvable:
	JMP CreatePuzzle

puzzleCreated:
	pop di
	pop si
	pop dx
	pop cx
	pop bx
	pop ax

	ret
endp GeneratePuzzle


GenerateRandomNumber proc
	; random number from 0 to 65534.
	; uses the xorshfit algorithm
	; xs ^= xs << 7;
	; xs ^= xs >> 9;
	; xs ^= xs << 8;
	; reference: http://www.retroprogramming.com/2017/07/xorshift-pseudorandom-numbers-in-z80.html

	; Expects: AX = The seed
	; Returns: AX = The randomly generated number
	push	bx
	push	cx
	push	dx

	mov	dx, ax

	; XOR shift left by 7
	shl	ax, 7	
	xor	ax, dx

	mov	dx, ax
	
	; XOR shift right by 9
	shr	ax, 9
	xor	ax, dx

	mov	dx, ax
	
	; XOR shift left by 8
	shl	ax, 8
	xor	ax, dx

	pop	dx
	pop	cx
	pop	bx
	
	ret
endp GenerateRandomNumber


ConvertRandomNumberToPercent proc
	; random number to range from 0 to 99.

	; Expects: AX = The random number input
	; Returns: AX = A random number from 0 to 99.
	push	bx
	push	cx
	push	dx

	xor	dx, dx

	mov	cx, 100		; We multiply by 100 to accomadete the decimal point
	mul	cx

	mov	cx, 65535	; We set the highest possible value as the divisor
	div	cx			; We do the division
	
	pop	dx
	pop	cx
	pop	bx
	
	ret	
endp ConvertRandomNumberToPercent


ConvertRandomNumberToRange proc
	; random num to a range.

	; Expects: AX = The random number, BX = The smallest range, DX = The highest range
	; Returs: AX = The randomly generated number
	push	bx
	push	cx
	push	dx

	push	bx		; We will use this at the end
	
	call	ConvertRandomNumberToPercent

	sub	dx, bx		; We get the range size
	mul	dx			; We multiply
		
	xor	dx, dx
	mov	cx, 100		
	div	cx			; We then divide by 100

	pop	bx
	add	ax, bx		; We add the smallest range to the result

	pop	dx
	pop	cx
	pop	bx

	ret
endp ConvertRandomNumberToRange	

GenerateSeed proc
	push ax
	push dx

	; Get the time
	mov	ah, 2ch
	int	21h

	; Do some computations to the returned time to have a more random seed.
	mov	ah, cl
	add	ah, 1		; Add 1 to ensure we never get zero seed
	mul	dh
	mov	al, ch
	add	al, 1		; Add 1 to ensure we never get zero seed
	mul	dh
	add	al, 1		; Add 1 to ensure we never get zero seed
	
	mov seed, ax
	
	pop dx
	pop ax
	
	ret
endp GenerateSeed


