PlayGame proc

checkMouse:
	mov  bx, 0           ; check if left click
	call GetMouseState
	and  bx, 00000001b ; check second bit for left click
	jz   checkMouse      ; no left click repeat 

	mov cursor_x, cx
	mov cursor_y, dx
	
checkClick:
	push ax
	push bx
	push cx
	
	mov cx, 0 ; column index
	mov bx, 0 ; index of tile x coord
	mov si, cursor_x
	
checkX:
	
	; check if  cursor_x is in tileCoordX[ax] to tileCoordX[ax] + 30
	cmp si, tileCoordX[bx]
	JNGE nextX				
	; if cursor x greater or equal than coord at x 
	mov ax, tileCoordX[bx]				; max range, end x coord of tile
	add ax,  29
	
	cmp si, ax
	JNLE nextX 				; if out of button range skip to next x
	
	; is in X range of tile
	mov si, cursor_y
	jmp checkY

nextX:
	inc bx
	inc bx
	
	inc cx
	cmp cx, w			; end of column
	JNE checkX
		
	; skip to end, not in range of any tiles
	JMP endClickCheck
	
checkY:
	cmp si, tileCoordY[bx]
	JNGE nextRow
	; if cursor y greater or equal than coord at y
	mov ax, tileCoordY[bx]				; max range, end y coord of tile
	add ax,  29
	
	cmp si, ax
	JNLE nextRow
	; is in X and Y range of tile, tile is bx-th tile of puzzle tiles array
	mov cursor_tile, bx	; save selected tile
	
	; do stuff on click
	call MovePiece
	JMP endClickCheck
	
nextRow:
	; go to next row, bx index + w (number of tiles in width of board)
	; add 2, to go to next index, w times

	; save use di to use as counter of w times
	mov di, 0
	
incrementIndex:
	inc bx
	inc bx
		
	mov ax, tileCoordY[bx]
	cmp ax, 'e' 		; end of tiles
	JE endClickCheck	; exit check, no tile found
	
	inc di
	cmp di, w	
	JNE incrementIndex
	
	JMP checkY
	
	;end of check, no found tile if reached here
endClickCheck:
	pop cx
	pop bx
	pop ax
	
	call PlayGame
	
	ret
endp PlayGame


MovePiece proc
	push ax
	push bx
	push cx
	push dx 
	push di
	push si
	
checkNextToEmpty:
	mov bx, cursor_tile ; index of tile
	mov ax, tiles[bx]	; value of tile on array
	mov di, w
	add di, w			; convert to 2 byte index for cmp
	
	; top: index - w (number of columns), if index < w, no pieces on top
	; bottom: index + w, if index + w greater than w * h, number of tiles, no pieces on bottom
	; left: index - 1, if index % w = 0, no piecs on left
	; right: index +1, if index +1 % w = 0, no pieces on right
checkTop:
	cmp bx, di
	JL checkBottom
	; do nothing if bx < w, check next side
	; else check piece on top
	
	push bx
	sub bx, di 			; get piece on top
	mov si, bx			; save index of top tile
	mov cx, tiles[bx] 	; get tile piece number
	pop bx
	
	cmp cx, 0 			; check if 0, empty piece
	JE move				; if it is 0, empty, move piece to there, else continue check

checkBottom:
	mov cx, bx
	add cx, di			

	cmp cx, puzzleLength	; if index + w greater than w * h, number of tiles, no pieces on bottom
	JG checkLeft
	; do nothing if cx > number of tiles
	; else check piece on bottom
	
	push bx
	add bx, di 			; get piece on bottom
	mov si, bx			; save index of bottom tile
	mov cx, tiles[bx] 	; get tile piece number
	pop bx
	
	cmp cx, 0 			; check if 0, empty piece
	JE move				; if it is 0, empty, move piece to there, else continue check
	
checkLeft:
	push ax		; save ax, bx, registers used for mod
	push bx
	
	mov dx, 0	; remainder
	mov ax, bx	; ax number getting divided
	mov bx,	di	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	pop bx
	pop ax
	
	cmp dx, 0
	JE checkRight
	; index % w = 0, no pieces on left, no nothing
	; else check piece on left
	
	push bx
	dec bx				; subtract 2 btye index
	dec bx				; get piece on left index
	mov si, bx			; save index of left tile
	mov cx, tiles[bx] 	; get tile piece number
	pop bx
	
	cmp cx, 0 			; check if 0, empty piece
	JE move				; if it is 0, empty, move piece to there, else continue check

checkRight:
	push ax		; save ax, bx, registers used for mod
	push bx
	
	inc bx
	inc bx
	
	mov dx, 0	; remainder
	mov ax, bx	; ax number getting divided
	mov bx,	di	; bx number dividing by
	div bx		; divide ax by bx, remainder stored in dx
	
	pop bx
	pop ax
	
	cmp dx, 0
	JE doNothing
	; index +1 % w = 0, no pieces on right, no nothing
	; else check piece on right
	
	push bx
	inc bx				; add 2 btye index
	inc bx				; get piece on right index
	mov si, bx			; save index of right tile
	mov cx, tiles[bx] 	; get tile piece number
	pop bx
	
	cmp cx, 0 			; check if 0, empty piece
	JE move				; if it is 0, empty, move piece to there, else continue check
	
	JMP doNothing
	
 move:
	; si index of empty tile
	; bx index of selected tile
	; switch values in index

	push ax
	push cx
	push si

	mov ax, tiles[bx]		; selected tile
	
	push bx
	
	mov bx, si
	mov cx, tiles[bx]		; empty tile
	mov tiles[bx], ax 		; replace empty tile with selected
	
	pop bx
	mov tiles[bx], cx		; replace selected with empty
	
	pop si
	pop cx
	pop ax

	; refresh board
	call CheckWin
	
	call FindTileCoords		; fills tileCoordX, tileCoordY
	
	mov ax, 2h				; hide cursor to draw under it
	int 33h
	
	call DrawTiles			; draw tiles in position
	
	mov ax, 1h				; show cursor again
	int 33h 
	
doNothing:
	pop si
	pop di
	pop dx
	pop cx
	pop bx
	pop ax
	
	ret
endp MovePiece


DisplayFrame proc
	push ax
	push cx
	push dx
	
	mov cx, 0
	mov dx, 0
	
	mov al, 9
	mov ah, 0ch 
	int 10h
	
fillBackground:
	inc cx
	int 10h
	
	cmp cx, 320
	JNE fillBackground

	mov cx, 0 	 ; reset to start of col
	inc dx     	 ; next row
	cmp dx, 200
	JNE fillBackground

	pop dx
	pop cx
	pop ax


	mov si, -1

	mov cx, 20 ;x pos
	mov dx, 20 ;y pos
	
	call GetHLineLength ; get horizontal line length
	add hLineLength, cx
	
	call GetVLineLength ; get vertical line length
	add vLineLength, dx
	
	mov al, 6 	; color
	mov ah, 0ch ; put pixel

; draw horizontal lines	
	push cx
	push dx

drawHLine:
	inc cx
	int 10h ; draw pixel
	
	push dx
	inc dx
	int 10h ; draw pixel
	pop dx

	cmp cx, hLineLength
	JNE drawHLine 
	
	mov cx, 20  ; reset to start of col
	
	inc si 		; si as counter
	inc dx 
	inc dx
	add dx, 30 	; add width of tiles
	
	cmp si, h
	JNE drawHLine
	
; draw vectical lines	
	pop dx
	pop cx
	
	dec vLineLength
	dec dx
	mov si, -1 ; reset counter
	
drawVLine:
	inc dx
	int 10h ; draw pixel
	
	push cx
	inc cx
	int 10h ; draw pixel
	pop cx

	cmp dx, vLineLength
	JNE drawVLine 
	
	mov dx, 20  ; reset to start of col
	dec dx		
	
	inc si 		; si as counter
	inc cx 		
	inc cx
	add cx, 30 	; add width of tiles
	
	cmp si, w
	JNE drawVLine
	
	ret
endp DisplayFrame


